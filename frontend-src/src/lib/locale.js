/** 地區（危機熱線）與語言（UI + AI 回覆） */

export const REGION_KEY = 'mindlab_region';
export const LANG_KEY = 'mindlab_lang';

export const REGION_OPTIONS = [
  { value: 'HK', label: '🇭🇰 香港' },
  { value: 'TW', label: '🇹🇼 台灣' },
  { value: 'GB', label: '🇬🇧 UK' },
];

export const LANG_OPTIONS = [
  { value: 'zh-HK', label: '廣東話' },
  { value: 'zh-TW', label: '繁體中文（台灣）' },
  { value: 'en', label: 'English' },
];

const REGION_DEFAULT_LANG = { HK: 'zh-HK', TW: 'zh-TW', GB: 'en' };

export function getRegion() {
  if (typeof window === 'undefined') return 'HK';
  return sessionStorage.getItem(REGION_KEY) || 'HK';
}

export function getLanguage() {
  if (typeof window === 'undefined') return 'zh-HK';
  return localStorage.getItem(LANG_KEY) || 'zh-HK';
}

export function normalizeLanguage(lang) {
  if (lang === 'en' || lang === 'zh-TW' || lang === 'zh-HK') return lang;
  if (lang === 'zh') return 'zh-HK';
  return 'zh-HK';
}

export function setRegion(region) {
  if (typeof window === 'undefined') return;
  const upper = ['HK', 'TW', 'GB'].includes(region?.toUpperCase()) ? region.toUpperCase() : 'HK';
  sessionStorage.setItem(REGION_KEY, upper);
  window.dispatchEvent(new Event('mindlab-locale-changed'));
}

export function setLanguage(lang) {
  if (typeof window === 'undefined') return;
  const safe = normalizeLanguage(lang);
  localStorage.setItem(LANG_KEY, safe);
  window.dispatchEvent(new Event('mindlab-locale-changed'));
}

/** 切換地區時一併更新語言（與個人設定預期一致） */
export function setRegionAndLanguage(region) {
  if (typeof window === 'undefined') return;
  const upper = ['HK', 'TW', 'GB'].includes(region?.toUpperCase()) ? region.toUpperCase() : 'HK';
  setRegion(upper);
  setLanguage(REGION_DEFAULT_LANG[upper] || 'zh-HK');
}

export function isEnglish(lang = getLanguage()) {
  return normalizeLanguage(lang) === 'en';
}

export function isTaiwan(lang = getLanguage()) {
  return normalizeLanguage(lang) === 'zh-TW';
}

export function languageForApi(lang = getLanguage()) {
  return normalizeLanguage(lang);
}

export function localeToContextOptions(lang = getLanguage()) {
  const safe = normalizeLanguage(lang);
  return {
    english: safe === 'en',
    language: safe,
    taiwan: safe === 'zh-TW',
  };
}

/** LLM 回覆用嘅口語指令 */
export function replyStyleInstruction(lang = getLanguage()) {
  const safe = normalizeLanguage(lang);
  if (safe === 'en') {
    return 'Reply in natural, warm English (2–4 sentences).';
  }
  if (safe === 'zh-TW') {
    return '用台灣繁體中文、自然口語（國語）回覆，2至4句即可；唔好用粵語或過度書面語。';
  }
  return '用自然嘅廣東話口語回覆，2至4句即可；唔好用書面語或普通話。';
}

