// ── 品牌用语合规过滤 (合规审查报告 §7.1-7.3) ──
// 黑名单词库 — 前端预过滤，后端二次过滤
export const BRAND_BLACKLIST: Record<RegionCode, string[]> = {
  hk: ['診斷', '治療', '治愈', '處方', '患者', '臨床', '保證治愈', '100%有效', 'AI取代心理師'],
  tw: ['診斷', '治療', '治愈', '處方', '患者', '臨床', '保證治愈', '100%有效', 'AI取代心理師', '諮詢'], // 台灣禁用「諮詢」替代「諮商」
  gb: ['diagnose', 'treat', 'cure', 'prescribe', 'therapy', 'patient', 'clinical', '100% effective', 'guaranteed cure', 'replaces therapists', 'AI therapy', 'wellness'], // 🇬🇧禁用「wellness」用「wellbeing」
};

export function filterBrandCompliance(text: string, region: RegionCode): { clean: boolean; violations: string[] } {
  const blacklist = BRAND_BLACKLIST[region];
  const violations = blacklist.filter((word) => text.toLowerCase().includes(word.toLowerCase()));
  return { clean: violations.length === 0, violations };
}
